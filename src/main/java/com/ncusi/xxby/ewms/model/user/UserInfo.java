package com.ncusi.xxby.ewms.model.user;

public class UserInfo {

	private String id;
	private String sex;
	private String address;
	private String kind;
	private String connect;
	private String checkid;
	private String checkwork;
	private String checkmail;
	private String checkphone;
	private String other;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getKind() {
		return kind;
	}

	public void setKind(String kind) {
		this.kind = kind;
	}

	public String getConnect() {
		return connect;
	}

	public void setConnect(String connect) {
		this.connect = connect;
	}

	public String getCheckid() {
		return checkid;
	}

	public void setCheckid(String checkid) {
		this.checkid = checkid;
	}

	public String getCheckwork() {
		return checkwork;
	}

	public void setCheckwork(String checkwork) {
		this.checkwork = checkwork;
	}

	public String getCheckmail() {
		return checkmail;
	}

	public void setCheckmail(String checkmail) {
		this.checkmail = checkmail;
	}

	public String getCheckphone() {
		return checkphone;
	}

	public void setCheckphone(String checkphone) {
		this.checkphone = checkphone;
	}

	public String getOther() {
		return other;
	}

	public void setOther(String other) {
		this.other = other;
	}

}
